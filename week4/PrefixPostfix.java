public class PrefixPostfix {
    public static void main(String[] args) {
        int prefixValue=1;
        // note theat the value changes in same line
        System.out.println(++prefixValue);
        System.out.println(prefixValue);
        int postFix=20;
        // note that the value doesnot change in sameline
        System.out.println(postFix++);
        System.out.println(postFix);
        

    }
    
}
